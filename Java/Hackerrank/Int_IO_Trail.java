import java.io.*;
import java.util.*;

public class Int_IO_trail {

    public static void main(String[] args) {
        Scanner i=new Scanner(System.in);
        int i1=i.nextInt();
        int i2=i.nextInt();
        int i3=i.nextInt();
        i.close();
        
        System.out.println(i1+"\n"+i2+"\n"+i3);
    }
}